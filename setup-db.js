    let currentStmt = '';
    
    for (const line of rlsLines) {
      currentStmt += ' ' + line;
      if (line.includes(';')) {
        const stmt = currentStmt.trim();
        if (stmt && stmt !== ';') {
          try {
            await sql.unsafe(stmt);
          } catch (e) {
            if (e.message.includes('already exists')) {
              // Policy j√° existe, ignora
            } else {
              console.error('Erro em statement:', stmt.substring(0, 50) + '...');
              throw e;
            }
          }
        }
        currentStmt = '';
      }
    }
    console.log('‚úÖ RLS policies criadas!');

